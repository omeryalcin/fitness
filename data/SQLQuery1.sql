CREATE TABLE ABONE
(
	AboneID INT NOT NULL PRIMARY KEY IDENTITY(1,1), 
	KayitNo INT NOT NULL UNIQUE,
	Ad NVARCHAR(20), 
	Soyad NVARCHAR(20),
	Boy INT,
	Kilo INT,
	TamOgrenci BIT,
	Cinsiyet BIT,
	UyeBaslangic DATETIME,
	UyeBitis DATETIME, 
	Telefon NVARCHAR(11),
);

CREATE TABLE KULLANICI
(
	KullaniciID INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	KullaniciAdi NVARCHAR(20),
	Parola NVARCHAR(16),
	Ad NVARCHAR(20), 
	Soyad NVARCHAR(20),
);



CREATE TABLE KAYDEDEN
(
	AboneID INT FOREIGN KEY REFERENCES ABONE(AboneID),
	KullaniciID INT FOREIGN KEY REFERENCES KULLANICI(KullaniciID),
);
GO

